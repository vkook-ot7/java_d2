class Relational
{
    public static void main(String args[])
    {
        int a,b;
        a=76;
        b=77;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println(a!=b);
        System.out.println(a==b);
        
    }
}